#!/usr/bin/env bash
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
WORKSPACE_PATH="${CONDUCTOR_WORKSPACE_PATH:-$REPO_ROOT}"
WORKSPACE_NAME="${CONDUCTOR_WORKSPACE_NAME:-$(basename "$WORKSPACE_PATH")}"
BASE_PORT="${CONDUCTOR_PORT:-3000}"

WEB_PORT="$BASE_PORT"
API_PORT="$((BASE_PORT + 1))"
DB_PORT="$((BASE_PORT + 2))"
SHADOW_DB_PORT="$((BASE_PORT + 3))"
STUDIO_PORT="$((BASE_PORT + 4))"
INBUCKET_PORT="$((BASE_PORT + 5))"
EDGE_INSPECTOR_PORT="$((BASE_PORT + 6))"
ANALYTICS_PORT="$((BASE_PORT + 7))"

CONDUCTOR_DIR="$REPO_ROOT/.context/conductor"
SUPABASE_WORKDIR="$CONDUCTOR_DIR/supabase"
SUPABASE_PROJECT_DIR="$SUPABASE_WORKDIR/supabase"
WEB_ENV_FILE="$REPO_ROOT/apps/web/.env.local"
CLI_ENV_FILE="$REPO_ROOT/.env"
GENERATED_MARKER="# Generated by scripts/conductor.sh."

cd "$REPO_ROOT"

log() {
  printf '[conductor] %s\n' "$*"
}

require_command() {
  if ! command -v "$1" >/dev/null 2>&1; then
    printf '[conductor] Missing required command: %s\n' "$1" >&2
    exit 1
  fi
}

ensure_env_writable() {
  local file="$1"

  if [ ! -e "$file" ]; then
    return 0
  fi

  if [ "${CONDUCTOR_OVERWRITE_ENV:-}" = "1" ]; then
    return 0
  fi

  if grep -Fq "$GENERATED_MARKER" "$file"; then
    return 0
  fi

  printf '[conductor] Refusing to overwrite existing non-generated env file: %s\n' "$file" >&2
  printf '[conductor] Move it aside or set CONDUCTOR_OVERWRITE_ENV=1 to replace it.\n' >&2
  exit 1
}

workspace_hash() {
  printf '%s' "$WORKSPACE_PATH" | shasum -a 256 | awk '{ print substr($1, 1, 10) }'
}

sanitize_workspace_name() {
  printf '%s' "$WORKSPACE_NAME" \
    | tr '[:upper:]' '[:lower:]' \
    | sed -E 's/[^a-z0-9]+/-/g; s/-+/-/g; s/^-//; s/-$//'
}

project_id() {
  local hash max_name_len name prefix
  prefix="comp-v2"
  name="$(sanitize_workspace_name)"
  hash="$(workspace_hash)"
  max_name_len="$((40 - ${#prefix} - ${#hash} - 2))"

  if [ -z "$name" ]; then
    name="workspace"
  fi

  if [ "${#name}" -gt "$max_name_len" ]; then
    name="${name:0:$max_name_len}"
    name="${name%-}"
  fi

  printf '%s-%s-%s' "$prefix" "$name" "$hash"
}

ensure_tooling() {
  require_command node
  require_command corepack
  require_command python3
  require_command curl
  require_command docker
  require_command shasum
  require_command supabase
}

enable_corepack() {
  log "Enabling Corepack"
  corepack enable
}

install_dependencies() {
  require_command pnpm
  log "Installing dependencies"
  pnpm install
}

prepare_supabase_workdir() {
  local id web_url api_url
  id="$(project_id)"
  web_url="http://127.0.0.1:${WEB_PORT}"
  api_url="http://127.0.0.1:${API_PORT}"

  mkdir -p "$SUPABASE_PROJECT_DIR"

  ln -sfn "$REPO_ROOT/supabase/migrations" "$SUPABASE_PROJECT_DIR/migrations"
  ln -sfn "$REPO_ROOT/supabase/seed.sql" "$SUPABASE_PROJECT_DIR/seed.sql"

  cat >"$SUPABASE_PROJECT_DIR/config.toml" <<EOF
# Generated by scripts/conductor.sh. Do not edit by hand.
project_id = "$id"

[api]
enabled = true
port = $API_PORT
schemas = ["public", "storage"]
extra_search_path = ["public", "extensions"]
max_rows = 1000

[db]
port = $DB_PORT
shadow_port = $SHADOW_DB_PORT
major_version = 15

[studio]
enabled = true
port = $STUDIO_PORT
api_url = "$api_url"

[inbucket]
enabled = true
port = $INBUCKET_PORT

[auth]
enabled = true
site_url = "$web_url"
additional_redirect_urls = ["$web_url/auth/callback"]
jwt_expiry = 3600
enable_signup = true

[auth.email]
enable_signup = true
enable_confirmations = false

[storage]
enabled = true
file_size_limit = "25MiB"

[edge_runtime]
enabled = true
inspector_port = $EDGE_INSPECTOR_PORT

[analytics]
enabled = true
port = $ANALYTICS_PORT
backend = "postgres"
EOF
}

supabase_project_id() {
  project_id
}

supabase_container_project_id() {
  local name="$1"

  case "$name" in
    supabase_analytics_*) printf '%s' "${name#supabase_analytics_}" ;;
    supabase_edge_runtime_*) printf '%s' "${name#supabase_edge_runtime_}" ;;
    supabase_pg_meta_*) printf '%s' "${name#supabase_pg_meta_}" ;;
    supabase_realtime_*) printf '%s' "${name#supabase_realtime_}" ;;
    supabase_inbucket_*) printf '%s' "${name#supabase_inbucket_}" ;;
    supabase_storage_*) printf '%s' "${name#supabase_storage_}" ;;
    supabase_studio_*) printf '%s' "${name#supabase_studio_}" ;;
    supabase_vector_*) printf '%s' "${name#supabase_vector_}" ;;
    supabase_auth_*) printf '%s' "${name#supabase_auth_}" ;;
    supabase_kong_*) printf '%s' "${name#supabase_kong_}" ;;
    supabase_rest_*) printf '%s' "${name#supabase_rest_}" ;;
    supabase_db_*) printf '%s' "${name#supabase_db_}" ;;
    *) return 1 ;;
  esac
}

docker_port_match() {
  local ports="$1"
  local port="$2"

  printf '%s' "$ports" | grep -Eq "(^|[ ,])([^ ]+:)?${port}->"
}

port_conflict_message() {
  local label="$1"
  local port="$2"
  local current_project="$3"
  local name ports project lsof_owner

  while IFS=$'\t' read -r name ports; do
    if ! docker_port_match "$ports" "$port"; then
      continue
    fi

    if project="$(supabase_container_project_id "$name")"; then
      if [ "$project" = "$current_project" ]; then
        return 1
      fi

      printf '%s port %s is already used by Supabase project %s (%s)\n' "$label" "$port" "$project" "$name"
      return 0
    fi

    printf '%s port %s is already used by Docker container %s\n' "$label" "$port" "$name"
    return 0
  done < <(docker ps --format '{{.Names}}\t{{.Ports}}')

  lsof_owner="$(lsof -nP -iTCP:"$port" -sTCP:LISTEN 2>/dev/null | awk 'NR == 2 { print $1 " pid " $2 }')"
  if [ -n "$lsof_owner" ]; then
    printf '%s port %s is already used by %s\n' "$label" "$port" "$lsof_owner"
    return 0
  fi

  return 1
}

blocking_supabase_projects() {
  sed -nE 's/.*Supabase project ([^ ]+).*/\1/p' | sort -u
}

ensure_conductor_ports_available() {
  local id conflicts entry label message port projects
  id="$(supabase_project_id)"
  conflicts=""

  for entry in \
    "api:${API_PORT}" \
    "db:${DB_PORT}" \
    "shadow db:${SHADOW_DB_PORT}" \
    "studio:${STUDIO_PORT}" \
    "inbucket:${INBUCKET_PORT}" \
    "edge inspector:${EDGE_INSPECTOR_PORT}" \
    "analytics:${ANALYTICS_PORT}"; do
    label="${entry%:*}"
    port="${entry##*:}"
    if message="$(port_conflict_message "$label" "$port" "$id")"; then
      conflicts="${conflicts}${message}"$'\n'
    fi
  done

  if [ -z "$conflicts" ]; then
    return 0
  fi

  printf '[conductor] Cannot start isolated Supabase project %s because Conductor ports are already in use:\n' "$id" >&2
  printf '%s' "$conflicts" | sed 's/^/[conductor] - /' >&2

  projects="$(printf '%s' "$conflicts" | blocking_supabase_projects)"
  if [ -n "$projects" ]; then
    printf '[conductor] Stop the blocking Supabase project(s), then run setup again:\n' >&2
    while IFS= read -r project; do
      printf '[conductor]   supabase stop --project-id %s --no-backup\n' "$project" >&2
    done <<<"$projects"
  else
    printf '[conductor] Free the listed port(s), then run setup again.\n' >&2
  fi

  exit 1
}

ensure_web_port_available() {
  local message

  if message="$(port_conflict_message "web" "$WEB_PORT" "$(supabase_project_id)")"; then
    printf '[conductor] Cannot start web server because the Conductor web port is already in use:\n' >&2
    printf '[conductor] - %s\n' "$message" >&2
    printf '[conductor] Stop the process using port %s, then run again.\n' "$WEB_PORT" >&2
    exit 1
  fi
}

ensure_supabase_running() {
  local id api_url status
  id="$(supabase_project_id)"

  if ! docker ps --format '{{.Names}}' | grep -Eq "^supabase_.*_${id}$"; then
    printf '[conductor] Supabase project %s is not running for this workspace.\n' "$id" >&2
    printf '[conductor] Run setup first for this workspace, then run again.\n' >&2
    exit 1
  fi

  if ! status="$(status_json 2>/dev/null)"; then
    printf '[conductor] Supabase project %s is running, but status is not readable.\n' "$id" >&2
    printf '[conductor] Run setup first for this workspace, then run again.\n' >&2
    exit 1
  fi

  api_url="$(printf '%s' "$status" | json_value API_URL)"
  if [ -z "$api_url" ]; then
    printf '[conductor] Supabase project %s did not report an API URL.\n' "$id" >&2
    printf '[conductor] Run setup first for this workspace, then run again.\n' >&2
    exit 1
  fi
}

start_supabase() {
  prepare_supabase_workdir
  ensure_conductor_ports_available
  log "Using isolated Supabase project $(supabase_project_id)"
  log "Starting isolated Supabase stack on ports ${API_PORT}-${ANALYTICS_PORT}"
  supabase start --workdir "$SUPABASE_WORKDIR"
  ensure_storage_gateway
}

reset_supabase_db() {
  local output_file exit_code
  output_file="$(mktemp)"

  log "Resetting isolated Supabase database"
  set +e
  supabase db reset --workdir "$SUPABASE_WORKDIR" >"$output_file" 2>&1
  exit_code="$?"
  set -e

  if [ "$exit_code" -eq 0 ]; then
    cat "$output_file"
    ensure_storage_gateway
    rm -f "$output_file"
    return 0
  fi

  if grep -q 'Restarting containers' "$output_file" && grep -q 'Error status 502' "$output_file"; then
    sed '/Error status 502/,$d' "$output_file"
    log "Supabase reset completed migrations and seed; waiting for the restarted stack"
    wait_for_supabase_status
    ensure_storage_gateway
    rm -f "$output_file"
    return 0
  fi

  cat "$output_file" >&2
  rm -f "$output_file"
  return "$exit_code"
}

status_json() {
  supabase status --workdir "$SUPABASE_WORKDIR" -o json \
    | python3 -c 'import sys
raw = sys.stdin.read()
start = raw.find("{")
end = raw.rfind("}")
if start == -1 or end == -1 or end < start:
    raise SystemExit("Supabase status did not include JSON")
print(raw[start:end + 1])
'
}

json_value() {
  local key="$1"
  python3 -c 'import json,sys; print(json.load(sys.stdin).get(sys.argv[1], ""))' "$key"
}

wait_for_supabase_status() {
  local attempt status api_url

  for attempt in $(seq 1 30); do
    if status="$(status_json 2>/dev/null)"; then
      api_url="$(printf '%s' "$status" | json_value API_URL)"
      if [ -n "$api_url" ]; then
        log "Supabase stack recovered"
        return 0
      fi
    fi

    sleep 1
  done

  printf '[conductor] Supabase stack did not recover after db reset\n' >&2
  exit 1
}

storage_gateway_code() {
  curl -s -o /dev/null -w '%{http_code}' --max-time 5 "http://127.0.0.1:${API_PORT}/storage/v1/bucket" 2>/dev/null || true
}

ensure_storage_gateway() {
  local attempt code kong
  kong="supabase_kong_$(supabase_project_id)"

  for attempt in $(seq 1 20); do
    code="$(storage_gateway_code)"
    case "$code" in
      200|400|401|403)
        return 0
        ;;
    esac
    sleep 1
  done

  log "Restarting Supabase gateway so Storage is reachable"
  docker restart "$kong" >/dev/null

  for attempt in $(seq 1 20); do
    code="$(storage_gateway_code)"
    case "$code" in
      200|400|401|403)
        log "Supabase Storage gateway is reachable"
        return 0
        ;;
    esac
    sleep 1
  done

  printf '[conductor] Supabase Storage gateway did not become reachable (last HTTP %s)\n' "$code" >&2
  exit 1
}

write_env_files() {
  local status api_url anon_key service_role_key
  status="$(status_json)"
  api_url="$(printf '%s' "$status" | json_value API_URL)"
  anon_key="$(printf '%s' "$status" | json_value ANON_KEY)"
  service_role_key="$(printf '%s' "$status" | json_value SERVICE_ROLE_KEY)"

  if [ -z "$api_url" ] || [ -z "$anon_key" ] || [ -z "$service_role_key" ]; then
    printf '[conductor] Failed to read Supabase status output\n' >&2
    exit 1
  fi

  ensure_env_writable "$WEB_ENV_FILE"
  ensure_env_writable "$CLI_ENV_FILE"

  cat >"$WEB_ENV_FILE" <<EOF
$GENERATED_MARKER
NEXT_PUBLIC_SUPABASE_URL=$api_url
NEXT_PUBLIC_SUPABASE_ANON_KEY=$anon_key
SUPABASE_SERVICE_ROLE_KEY=$service_role_key
EOF

  cat >"$CLI_ENV_FILE" <<EOF
$GENERATED_MARKER
COMPANION_SUPABASE_URL=$api_url
COMPANION_SUPABASE_ANON_KEY=$anon_key
NEXT_PUBLIC_SUPABASE_URL=$api_url
NEXT_PUBLIC_SUPABASE_ANON_KEY=$anon_key
SUPABASE_SERVICE_ROLE_KEY=$service_role_key
EOF

  log "Wrote $WEB_ENV_FILE and $CLI_ENV_FILE"
}

run_web() {
  require_command pnpm
  log "Starting web on http://127.0.0.1:${WEB_PORT}"
  pnpm --filter @companion/web exec next dev --hostname 127.0.0.1 --port "$WEB_PORT"
}

setup() {
  ensure_tooling
  enable_corepack
  install_dependencies
  start_supabase
  write_env_files
}

run() {
  ensure_tooling
  enable_corepack
  prepare_supabase_workdir
  ensure_supabase_running
  ensure_web_port_available
  reset_supabase_db
  write_env_files
  run_web
}

archive() {
  local id output_file exit_code remaining
  require_command supabase
  require_command docker
  prepare_supabase_workdir
  id="$(supabase_project_id)"
  output_file="$(mktemp)"

  log "Stopping isolated Supabase project $id"
  set +e
  supabase stop --project-id "$id" --no-backup >"$output_file" 2>&1
  exit_code="$?"
  set -e

  remaining="$(docker ps --format '{{.Names}}' | grep -E "^supabase_.*_${id}$" || true)"
  if [ -n "$remaining" ]; then
    cat "$output_file" >&2
    printf '[conductor] Supabase containers are still running for project %s:\n%s\n' "$id" "$remaining" >&2
    rm -f "$output_file"
    exit 1
  fi

  if [ "$exit_code" -ne 0 ]; then
    cat "$output_file" >&2
    printf '[conductor] supabase stop exited with %s, but no running containers remain for %s.\n' "$exit_code" "$id" >&2
  fi

  rm -f "$output_file"
}

case "${1:-}" in
  setup)
    setup
    ;;
  run)
    run
    ;;
  archive)
    archive
    ;;
  *)
    printf 'Usage: %s {setup|run|archive}\n' "$0" >&2
    exit 2
    ;;
esac
